import math
n = int(input('Número: '))
raiz = math.sqrt(n)
potencia = math.pow(n, 3)
fatorial = math.factorial(n)

print(f'Raiz quadrada de {n} = {math.ceil(raiz):.2f}\n {n}³ = {potencia}\n Fatorial de {n} = {fatorial}')

